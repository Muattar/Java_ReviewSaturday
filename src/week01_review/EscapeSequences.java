package week01_review;

public class EscapeSequences {

    public static void main(String[] args) {
        // \t
        System.out.println("\tHello Batch 28 \tStudents");

        //\n newLine
        System.out.println("Hello Students\t\nHow are you all today?");

        // \" doubleQuote
        System.out.println("Hello \"Cydeo\" Batch 28 students");


        // \\
        System.out.println("    ^");
        System.out.println("   / \\");
        System.out.println("  /   \\");
        System.out.println("  -----");










    }

}
